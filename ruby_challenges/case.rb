fav_color = "blue"
case fav_color
when "red"
  puts "Red like fire!"
when "orange"
  puts "Orange like, well... an orange."
when "yellow"
  puts "Yellow daffodils are so pretty in the spring!"
when "green"
  puts "Have you been to the Emerald City in Oz"
when "blue"
  puts "Blue like the sky"
when "purple"
  puts "Purple plums are the tasties"
else
  puts "Hmm, well I don't know what that color is!"
end

weather = "sunny"
case weather
when "cloudy"
  puts "Time to work in the yard!"
when "rainy"
  puts "Don't forget your umbrella!"
when "snowy"
  puts "Great day to work from home!"
when "sunny"
  puts "Stylin' the sunglasses!"
when "cold"
  puts "Brrrr!"
when "hot"
  puts "Pass me an iced tea!"
else "Wait a minute, the weather changes!"
end
